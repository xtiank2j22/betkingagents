<p><span><strong>Settlement of matches, statistics and other data</strong></span></p>
<p><span>Void - means cancelled events or bets as a result of abandoned matches, postponed matches, played before scheduled, technical result (awarded,walkover), obvious errors, etc. Void bets are calculated with odds of 1.00.</span></p>
<p><span>Bets on football matches labelled “extra time” will be settled excluding penalty shootouts. Bets on penalty shootouts are accepted separately.</span></p>
<p><span>All bets are settled on the final score displayed at the end of the match. If a result is changed or declared void for any reason by sports’ governing body at any time following the end of the match, such change will not alter the result for betting customers and the original result will dictate any bets and/or winnings already paid.</span></p>
<p><span>In the case of ambiguity over the official result from match officials, the outcome will be determined by BetKing (acting reasonably) using information from independent sources.</span></p>
<p><span>If a team is disqualified, thrown out or otherwise removed from a league before the relevant season has started then all bets involving that team will be made void.</span></p>
