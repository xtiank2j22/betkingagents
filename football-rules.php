<p><span><strong>90 minutes play</strong></span></p>
<p> </p>
<p><span>Unless otherwise stated, all bets on football (soccer) markets apply to 90 minutes of play according to the match officials, plus any added injury or stoppage time. However extra time and penalty shoot-outs are not included. The only exceptions from this rule: matches that are scheduled for a different duration (e.g. 60, 70, 80 or 120 minutes) as agreed by the laws of the competition in which they are competing or agreed by both sides prior to kick-off ( i.e. Youth matches, where games last for 80 minutes). In such cases, if the match is played in the traditional format of two halves, all bets will be settled at the end of the agreed game length which includes time added on by the referee for stoppages (whether the game length is advertised by BetKing or not). </span></p>
<p><span>FIFA rules in respect to the duration of friendly youth matches:</span></p>
<p><span>for U18, U17, U16, U15 teams – 80 minutes (two halves of 40 minutes each);</span></p>
<p><span>U14 teams – 70 minutes (two halves of 35 minutes each), all 70 minutes of play must elapse for bets to stand; </span></p>
<p><span>U13 and U12 teams – 60 minutes (two halves of 30 minutes each), all 60 minutes of play must elapse for bets to stand.</span></p>
<p><span>The rules of friendlies are determined before the start of a tournament. The Customer should check the rules from independent sources of information prior to placing bets on friendlies.</span></p>
<p><span>If the match is played in an unusual format (e.g. 3 or 4 periods,90 minutes or less) then all half-time markets will be voided but all other markets will be settled on the basis of the score at the end of the match (including any time added on by the referee for stoppages).All placed bets will be voided if the match is officially declared as a 'no classify'. (4 x 30 minutes and all other variations apart from stated rules).</span></p>
<p> </p>
<p><span><strong>Delayed, moved or suspended matches</strong></span></p>
<p><span>If an event or game as a single bet is cancelled, the bet will be considered with odd 1.00 and the stake will be returned to the player. If an event or game in a double or multiple bet is cancelled, the cancelled bet will be considered with odd 1.00 and all other bets odds remain normal. In any case of an obvious mistake or error identified by us, we reserve the right to cancel the bet in whole or in part or to rectify the mistake if applicable.</span></p>
<p><span>In cases where over 90% of the scheduled match time has been completed and the referee ends the match before the assigned time has elapsed then we will use that result for the settlement of all bets placed on the game. </span></p>
<p> </p>
<p><span>Unless otherwise stated for certain Sports/bet typologies, whenever an event is officially postponed or abandoned, all bets related to that event will remain valid if the event takes place and is completed within midnight (WAT) of the day following the date originally set for the match.</span></p>
<p><span>If it does not take place or is not completed within the mentioned time frame, all bets related to that event will be voided within 24 hours, except the bets which were placed on markets that have been unconditionally determined, which will stand.</span></p>
<p> </p>
<p><span>If a mach is restarted from the beginning, all bets placed before the market went in-play will stand, but any bets placed in-play will be voided, except when the bets were placed in-play on markets which have been unconditionally determined, which will stand. </span></p>
<p><span>Examples of unconditionally determined markets: </span></p>
<p><span>If you place a bet on Total goals in the first half being 2 or more and the match gets interrupted in the 40th minute at 1-1 result, the bet is won.</span></p>
<p><span>If you place a bet on 1/1 type for HT / FT market and the match is interrupted in the 60th minute with a 0-1 halftime score, then the bet is lost.</span></p>
<p><span>If you place a bet on 1/1 type for HT / FT market, and the match is interrupted in the 40th minute at 1-1 score, we are facing a situation where the bet will be void if the match is not played by 23:59 (local time) on its scheduled start date.</span></p>
