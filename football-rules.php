<p><span><strong>Matches not played as listed</strong></span></p>
<p><span>In case that any team starts the match with less than 11 players, all bets on that match will be made void.</span></p>
<p><span>Football (Soccer) kickoff dates and times displayed on BetKing website are an indication only and are not guaranteed to be correct. If an official fixture lists different team details to those listed on the BetKing website (e.g. official fixture specifies in the team name, Reserves/Age Group e.g. Under 21's/Gender e.g. Women, etc.), then all bets matched on the affected markets will be void. In all other cases, bets will stand (e.g. any wager on whether a team advances in a cup competition, or raises the cup, will have action regardless of a suspended or postponed match). </span></p>
<p><span>If the home team and away team for a listed match are reversed (e.g. match is played at the away team's ground) then bets placed on the affected markets will be void. For matches played at neutral venues, the team listed on the left are still classed as the 'home' team for settlement purposes.</span></p>
<p><span>A club team playing a European competition is classed as playing at home if the fixture is moved from the team's usual ground to another ground within their national boundaries, e.g. when Arsenal played their Champions League home ties at Wembley.</span></p>
